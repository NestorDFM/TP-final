    private void cargarTabla() {
		dtmCliente.setRowCount(0);
		Object[] fila = new Object[5];
		for (Cliente c:listaClientes) {
			fila[0]=c.getCodigo();
			fila[1]=c.getNombre();
			fila[2]=c.getApellido();
			fila[3]=c.getDireccion();
                        fila[4]=c.getCelular();
			dtmCliente.addRow(fila);
		}

	}
    
