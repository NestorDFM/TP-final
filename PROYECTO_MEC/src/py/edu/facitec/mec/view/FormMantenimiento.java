    private void consultar(int codigo) {
       if(codigo != 0){
           Mantenimiento man= mantenimientoDao.consultar(codigo);
           
           if(man != null){
               tfFecha.setText(man.getFecha());
               tfCodigoCli.setText(String.valueOf(man.getClienteCodigo()));
               tfCondicion.setText(man.getCondicion());
               tfImporte.setText(String.valueOf(man.getImporteTotal()));
               tfObservacion.setText(man.getObservacion());
               tfSituacion.setText(man.getSituacion());
               
           } else{
           JOptionPane.showMessageDialog(this, "No existe mantenimiento con este codigo!","Aviso",1);
           }
       }else{
          JOptionPane.showMessageDialog(this, "Debes digitar un numero!","Aviso",2); 
       }    
    }

