    private void insertar() {
        Mantenimiento man = new Mantenimiento();
       
        man.setFecha(tfFecha.getText());
        man.setClienteCodigo(Integer.parseInt(tfCodigoCli.getText()));
        man.setCondicion(tfCondicion.getText());
        man.setImporteTotal(Double.valueOf(tfImporte.getText()));
        man.setObservacion(tfObservacion.getText());
        man.setSituacion(tfSituacion.getText());
        
        mantenimientoDao.insertar(man);
        JOptionPane.showMessageDialog(this, "Se inserto el mantenimiento con exito", "Aviso", 1);
        limpiar();
    }
